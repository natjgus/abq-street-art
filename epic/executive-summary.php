		<h2>Executive Summary</h2>
		<p>ABQ Street Art is a mobile-first website that allows users to explore public art in Albuquerque. By integrating with Google Maps and pulling information about artwork from the <a href="http://coagisweb.cabq.gov/arcgis/rest/services/public/PublicArt/MapServer/0/query?where=1%3D1&text=&objectIds=&time=&geometry=&geometryType=esriGeometryEnvelope&inSR=&spatialRel=esriSpatialRelIntersects&relationParam=&outFields=*&returnGeometry=true&maxAllowableOffset=&geometryPrecision=&outSR=&returnIdsOnly=false&returnCountOnly=false&orderByFields=&groupByFieldsForStatistics=&outStatistics=&returnZ=false&returnM=false&gdbVersion=&returnDistinctValues=false&f=pjson" target="_blank"> Public Art Database,</a> users will be able to learn more about each piece of art and its artist. Users can also bookmark locations and comment on artwork photos. If we have time remaining after implementing these key features, we will also include one or two predetermined routes that users can follow.</p>
		<h3>System Goals</h3>
		<p>The ABQ Street Art mobile website will be the go-to resource for individuals interested in learning more about public art installations in Albuquerque. Casual users will be able to view a map of Albuquerque with pinned public art locations in their selected area and select a pin to learn more about the art itself. Casual users do not have to log in to use the map or to click location pins. On the other hand, early adopters and art enthusiasts will enjoy logging into the ABQ Street Art account, setting up a user profile, bookmarking location pins, and leaving comments.</p>
		<h3>Key Features</h3>
		<ul>
			<li>Mobile-first web application used by prolific and novice art enthusiasts </li>
			<li>Base interface features a map of public art locations in Albuquerque marked with pins</li>
			<li>Pins are clickable and provide more information: photo, artist, date created, etc.</li>
			<li>Users can view art pieces by category type, i.e. murals or public art; this will be a main menu item</li>
			<li>Users are able to log in and bookmark/favorite pieces of art</li>
		</ul>
			<h3>Stretch Goal</h3>
		<ul>
			<li>We are hoping to include 1-2 predetermined/suggested routes in Downtown Albuquerque that users can utilize (but not create themselves)</li>
		</ul>
